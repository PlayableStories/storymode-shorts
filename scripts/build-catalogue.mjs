// Regenerates GAMES.md from the markdown files in content/games/*.md.
//
// The markdown files are the single source of truth; this just refreshes the
// human-readable index that GitHub shows. Run it after adding or editing a
// game:
//
//   npm run catalogue
//
// Needs Node 18+ (remember: `nvm use 24`). Keep this script simple and
// readable — a non-developer should be able to follow it.

import fs from "node:fs";
import path from "node:path";
import matter from "gray-matter";

// Keep this list in sync with lib/themes.ts. It is only used to warn about
// unknown themes; the table prints whatever each game declares.
const THEMES = [
  "care",
  "memory",
  "identity",
  "place",
  "community",
  "politics",
  "lived experience",
];

const GAMES_DIR = path.join(process.cwd(), "content", "games");
const OUT = path.join(process.cwd(), "GAMES.md");

const games = fs
  .readdirSync(GAMES_DIR)
  .filter((f) => f.endsWith(".md"))
  .map((file) => {
    const { data } = matter(fs.readFileSync(path.join(GAMES_DIR, file), "utf8"));
    const themes = Array.isArray(data.themes) ? data.themes : [];
    for (const theme of themes) {
      if (!THEMES.includes(theme)) {
        console.warn(`  ! ${file}: unknown theme "${theme}" (ignored on site)`);
      }
    }
    return {
      title: data.title ?? file.replace(/\.md$/, ""),
      creator: data.creator ?? "Workshop participant",
      themes,
      workshop: data.workshop ?? "",
      play: data.play ?? "#",
      addedAt: data.addedAt ?? "",
    };
  })
  .sort((a, b) => (a.addedAt < b.addedAt ? 1 : -1));

// Escape pipes so they don't break the markdown table.
const cell = (value) => String(value).replace(/\|/g, "\\|");

const rows = games
  .map((g) => {
    const play =
      g.play && g.play !== "#" ? `[Play](${g.play})` : "_coming soon_";
    return `| ${cell(g.title)} | ${cell(g.creator)} | ${g.themes
      .map(cell)
      .join(", ")} | ${cell(g.workshop)} | ${play} |`;
  })
  .join("\n");

const md = `# Games

<!-- This file is generated by \`npm run catalogue\`. Do not edit it by hand. -->
<!-- The entries below are placeholder samples, to be replaced with real, consented games. -->

The games below were selected from Into Storymode's vibe coding workshops.
Each links out to where it can be played. This index is generated from the
markdown files in [\`content/games/\`](content/games) — the single source of truth.

| Title | Creator | Themes | Workshop | Play |
| --- | --- | --- | --- | --- |
${rows}

_${games.length} game${games.length === 1 ? "" : "s"} in the collection._
`;

fs.writeFileSync(OUT, md);
console.log(`Wrote ${games.length} games to GAMES.md`);
